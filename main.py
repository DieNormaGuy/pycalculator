
left = int(input("enter a number "))
right = int(input("enter a number "))
exp = input("expression ")
if exp == "+":
    sum = left + right 
    print (sum)
elif exp == "-":
    sum = left - right 
    print (sum)
elif exp == "*": 
    sum = left * right 
    print (sum)
elif exp == "/":
    sum = left / right 
    print (sum)
    
else :
    print ("invalid") 


